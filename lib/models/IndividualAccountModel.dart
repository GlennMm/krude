class IndividualAccountModel{
  var userName;
  var firstName;
  var lastName;
  var email;
  var country;
  var password;

  IndividualAccountModel(uname, fname, lname, em, contry, pasword){
    this.userName = uname;
    this.firstName = fname;
    this.lastName = lname;
    this.email = em;
    this.country = contry;
    this.password = pasword;
  }


}